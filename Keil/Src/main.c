#define KEY_PRESSED 		0x01
#define KEY_NOT_PRESSED 0x00

void waitForUserButtonPressed()
{
		uint32_t const delay = 100;
	  /* Waiting USER Button is Pressed */
    while (BSP_PB_GetState(BUTTON_KEY) == KEY_NOT_PRESSED) {
			BSP_LED_On(LED_GREEN);
			HAL_Delay(delay);
			BSP_LED_Off(LED_GREEN);
			
			BSP_LED_On(LED_ORANGE);
			HAL_Delay(delay);
			BSP_LED_Off(LED_ORANGE);
			
			BSP_LED_On(LED_RED);
			HAL_Delay(delay);
			BSP_LED_Off(LED_RED);
			
			BSP_LED_On(LED_BLUE);
			HAL_Delay(delay);
			BSP_LED_Off(LED_BLUE);
		}
}

void waitForUserButtonReleased()
{
	BSP_LED_Off(LED_GREEN);
	BSP_LED_Off(LED_ORANGE);
  BSP_LED_Off(LED_RED);
	while (BSP_PB_GetState(BUTTON_KEY) == KEY_PRESSED) {
		BSP_LED_Toggle(LED_BLUE);
		HAL_Delay(25);
	}
	BSP_LED_Off(LED_BLUE);
}

void fail(uint32_t loopCount)
{
	BSP_LED_Off(LED_RED);
	while (loopCount --> 0) {
		BSP_LED_Toggle(LED_RED);
		HAL_Delay(500);
	}
	BSP_LED_On(LED_RED);
  HAL_Delay(1000);
	waitForUserButtonPressed();
	waitForUserButtonReleased();
	BSP_LED_Off(LED_RED);
}

uint8_t sendKeyboardChar(uint8_t aChar, uint32_t timeout)
{
	uint8_t key_code = 0xFF;
	if (aChar >='1' && aChar <='9') {
		key_code = aChar - 19;
	} else if (aChar == '0') {
		key_code = 0x27;
	} else if (aChar == '\n' || aChar == '\r') {
		// Enter
		key_code = 0x28;
	}
	if (key_code == 0xFF) {
		fail(8);
		return key_code;
	}
	TM_USB_HIDDEVICE_Keyboard_t Keyboard;
	/* Set default values for keyboard struct */
	TM_USB_HIDDEVICE_KeyboardStructInit(&Keyboard);
	Keyboard.Key1 = key_code;
	TM_USB_HIDDEVICE_KeyboardSend(&Keyboard);
	BSP_LED_On(LED_ORANGE);
	HAL_Delay(timeout);
	/* Release all buttons */
	TM_USB_HIDDEVICE_KeyboardReleaseAll();
	BSP_LED_Off(LED_ORANGE);
	return aChar;
}
typedef struct
{
	uint8_t FirstByte;
	uint8_t Char;
	uint8_t ThirdByte;
	uint8_t ForthByte;
} TRX_DATA;
